"""Generate PWA / mobile shell from DoqlSpec.

Produces a self-contained installable PWA:
  build/mobile/
  ├── index.html          — mobile-first shell
  ├── manifest.json       — PWA metadata
  ├── service-worker.js   — offline cache + background sync
  ├── app.js              — entity CRUD client
  ├── style.css           — mobile-first styles
  ├── icons/              — (stub) SVG icons
  └── README.md

The PWA auto-detects a running API (same origin or env.API_HOST) and
renders a simple list/detail view per ENTITY.
"""
from __future__ import annotations

import json
import pathlib
import textwrap

from ..parser import DoqlSpec
from ..utils.naming import slug


def _gen_manifest(spec: DoqlSpec) -> str:
    slug_name = slug(spec.app_name)
    return json.dumps({
        "name": spec.app_name,
        "short_name": spec.app_name[:12],
        "description": f"Generated by doql v{spec.version}",
        "start_url": "./",
        "scope": "./",
        "display": "standalone",
        "orientation": "portrait",
        "background_color": "#0f172a",
        "theme_color": "#0ea5e9",
        "icons": [
            {"src": "icons/icon-192.svg", "sizes": "192x192", "type": "image/svg+xml", "purpose": "any"},
            {"src": "icons/icon-512.svg", "sizes": "512x512", "type": "image/svg+xml", "purpose": "any maskable"},
        ],
        "id": f"doql-{slug_name}",
        "categories": ["productivity", "utilities"],
    }, indent=2)


def _gen_service_worker(spec: DoqlSpec) -> str:
    slug_name = slug(spec.app_name)
    return textwrap.dedent(f"""\
        // Service worker for {spec.app_name}
        // Generated by doql — cache-first for shell, network-first for API.
        const CACHE = "doql-{slug_name}-v{spec.version}";
        const SHELL = [
          "./",
          "./index.html",
          "./app.js",
          "./style.css",
          "./manifest.json",
        ];

        self.addEventListener("install", (e) => {{
          self.skipWaiting();
          e.waitUntil(caches.open(CACHE).then((c) => c.addAll(SHELL)));
        }});

        self.addEventListener("activate", (e) => {{
          e.waitUntil(
            caches.keys().then((keys) =>
              Promise.all(keys.filter((k) => k !== CACHE).map((k) => caches.delete(k)))
            )
          );
          self.clients.claim();
        }});

        self.addEventListener("fetch", (e) => {{
          const url = new URL(e.request.url);
          // API calls — network first, fallback to cache
          if (url.pathname.startsWith("/api/") || url.pathname.startsWith("/auth/")) {{
            e.respondWith(
              fetch(e.request).then((r) => {{
                const copy = r.clone();
                caches.open(CACHE).then((c) => c.put(e.request, copy));
                return r;
              }}).catch(() => caches.match(e.request))
            );
            return;
          }}
          // Shell — cache first
          e.respondWith(
            caches.match(e.request).then((cached) => cached || fetch(e.request))
          );
        }});
        """)


def _gen_index_html(spec: DoqlSpec) -> str:
    entities = [e.name for e in spec.entities]
    return textwrap.dedent(f"""\
        <!doctype html>
        <html lang="en">
        <head>
          <meta charset="utf-8" />
          <meta name="viewport" content="width=device-width, initial-scale=1, viewport-fit=cover" />
          <meta name="theme-color" content="#0ea5e9" />
          <title>{spec.app_name}</title>
          <link rel="manifest" href="./manifest.json" />
          <link rel="stylesheet" href="./style.css" />
          <link rel="icon" type="image/svg+xml" href="./icons/icon-192.svg" />
        </head>
        <body>
          <header class="app-bar">
            <h1>{spec.app_name}</h1>
            <span id="status" class="status-chip">offline</span>
          </header>
          <nav class="tabs" id="tabs">
            {"".join(f'<button data-entity="{e}">{e}</button>' for e in entities) or '<em>No entities defined</em>'}
          </nav>
          <main id="list" class="list"></main>
          <button class="fab" id="fab-new" title="Create">+</button>
          <dialog id="form-dialog"><form id="entity-form" method="dialog"></form></dialog>
          <footer>
            <small>doql mobile · v{spec.version}</small>
          </footer>
          <script src="./app.js"></script>
        </body>
        </html>
        """)


def _gen_app_js(spec: DoqlSpec) -> str:
    entities_meta = [
        {
            "name": e.name,
            "path": f"/api/v1/{e.name.lower()}s",
            "fields": [{"name": f.name, "type": f.type, "required": f.required} for f in e.fields],
        }
        for e in spec.entities
    ]
    return textwrap.dedent(f"""\
        // {spec.app_name} — mobile PWA client (generated by doql)
        "use strict";

        const ENTITIES = {json.dumps(entities_meta, indent=2)};
        const API_BASE = (window.DOQL_API_BASE || "").replace(/\\/$/, "");
        const $status = document.getElementById("status");
        const $tabs = document.getElementById("tabs");
        const $list = document.getElementById("list");
        const $fab = document.getElementById("fab-new");
        const $dialog = document.getElementById("form-dialog");
        const $form = document.getElementById("entity-form");

        let current = ENTITIES[0] || null;

        function setStatus(ok) {{
          $status.textContent = ok ? "online" : "offline";
          $status.className = "status-chip " + (ok ? "ok" : "off");
        }}

        async function fetchJson(path, opts = {{}}) {{
          const r = await fetch(API_BASE + path, {{
            headers: {{ "Content-Type": "application/json" }},
            ...opts,
          }});
          if (!r.ok) throw new Error(r.status + " " + r.statusText);
          return r.status === 204 ? null : r.json();
        }}

        async function loadList() {{
          if (!current) {{ $list.innerHTML = "<em>No entities defined.</em>"; return; }}
          $list.innerHTML = "<em>Loading…</em>";
          try {{
            const items = await fetchJson(current.path);
            setStatus(true);
            $list.innerHTML = items.length
              ? items.map(renderCard).join("")
              : "<em>No records yet. Tap + to create one.</em>";
          }} catch (err) {{
            setStatus(false);
            $list.innerHTML = `<div class="err">API unreachable: ${{err.message}}</div>`;
          }}
        }}

        function renderCard(item) {{
          const title = item.name || item.title || item.email || item.id || "(untitled)";
          const subtitle = Object.entries(item)
            .filter(([k]) => !["id","name","title","email"].includes(k))
            .slice(0, 2)
            .map(([k,v]) => `${{k}}: ${{JSON.stringify(v)}}`)
            .join(" · ");
          return `<article class="card">
            <h3>${{String(title)}}</h3>
            <p>${{subtitle}}</p>
            <button class="del" data-id="${{item.id}}">delete</button>
          </article>`;
        }}

        $tabs.addEventListener("click", (e) => {{
          const btn = e.target.closest("button[data-entity]");
          if (!btn) return;
          current = ENTITIES.find((x) => x.name === btn.dataset.entity);
          document.querySelectorAll("#tabs button").forEach((b) => b.classList.toggle("active", b === btn));
          loadList();
        }});

        $list.addEventListener("click", async (e) => {{
          const del = e.target.closest("button.del");
          if (!del || !current) return;
          try {{
            await fetchJson(current.path + "/" + del.dataset.id, {{ method: "DELETE" }});
            loadList();
          }} catch (err) {{ alert("Delete failed: " + err.message); }}
        }});

        $fab.addEventListener("click", () => {{
          if (!current) return;
          $form.innerHTML = current.fields
            .filter((f) => !["id"].includes(f.name) && !f.name.endsWith("_at"))
            .map((f) => `<label>${{f.name}}<input name="${{f.name}}" ${{f.required ? "required" : ""}} /></label>`)
            .join("") + `<menu><button value="cancel">Cancel</button><button value="ok" type="submit">Save</button></menu>`;
          $dialog.showModal();
        }});

        $form.addEventListener("submit", async (e) => {{
          e.preventDefault();
          if (!current) return;
          const data = Object.fromEntries(new FormData($form).entries());
          try {{
            await fetchJson(current.path, {{ method: "POST", body: JSON.stringify(data) }});
            $dialog.close();
            loadList();
          }} catch (err) {{ alert("Create failed: " + err.message); }}
        }});

        // Activate first tab
        const first = $tabs.querySelector("button[data-entity]");
        if (first) first.classList.add("active");

        if ("serviceWorker" in navigator) {{
          navigator.serviceWorker.register("./service-worker.js").catch(console.warn);
        }}

        loadList();
        """)


