def generate(spec: DoqlSpec, env_vars: dict[str, str], out: pathlib.Path) -> None:
    """Generate API layer files into *out* directory."""
    if not spec.entities:
        readme = out / "README.md"
        readme.write_text(
            f"# {spec.app_name} — API\n\nNo entities defined — skipping code generation.\n",
            encoding="utf-8",
        )
        print(f"    → api/ (no entities, skipped)")
        return

    has_auth = bool(spec.roles)
    files = {
        "main.py":          _gen_main(spec),
        "database.py":      _gen_database(spec, env_vars),
        "models.py":        _gen_models(spec),
        "schemas.py":       _gen_schemas(spec),
        "routes.py":        _gen_routes(spec),
        "requirements.txt": _gen_requirements(has_auth=has_auth),
    }
    if has_auth:
        files["auth.py"] = _gen_auth(spec)
    for name, content in files.items():
        (out / name).write_text(content, encoding="utf-8")
        print(f"    → api/{name}")

    # Alembic migrations
    alembic_dir = out / "alembic" / "versions"
    alembic_dir.mkdir(parents=True, exist_ok=True)
    (out / "alembic.ini").write_text(_gen_alembic_ini(), encoding="utf-8")
    (out / "alembic" / "env.py").write_text(_gen_alembic_env(), encoding="utf-8")
    (alembic_dir / "001_initial.py").write_text(_gen_initial_migration(spec), encoding="utf-8")
    print(f"    → api/alembic.ini, alembic/env.py, alembic/versions/001_initial.py")

    readme = out / "README.md"
    readme.write_text(
        f"# {spec.app_name} — API\n\n"
        f"Generated by `doql build`.\n\n"
        f"## Quick start\n\n"
        f"```bash\n"
        f"cd build/api\n"
        f"pip install -r requirements.txt\n"
        f"uvicorn main:app --reload\n"
        f"```\n\n"
        f"## Entities\n\n"
        + "\n".join(f"- **{e.name}** ({len(e.fields)} fields)" for e in spec.entities)
        + "\n\n"
        f"## Endpoints\n\n"
        + "\n".join(
            f"- `GET /api/v1/{_snake(e.name)}s` · `POST` · `GET /:id` · `PATCH /:id` · `DELETE /:id`"
            for e in spec.entities
        )
        + "\n",
        encoding="utf-8",
    )
    print(f"    → api/README.md")


def export_openapi(spec: DoqlSpec, out: IO[str]) -> None:
    """Write OpenAPI 3.1 JSON to the given stream."""
    paths = {}
    for e in spec.entities:
        plural = _snake(e.name) + "s"
        base = f"/api/v1/{plural}"
        paths[base] = {
            "get": {
                "summary": f"List {e.name}s",
                "responses": {"200": {"description": "OK"}},
            },
            "post": {
                "summary": f"Create {e.name}",
                "responses": {"201": {"description": "Created"}},
            },
        }
        paths[f"{base}/{{id}}"] = {
            "get":    {"summary": f"Get {e.name}", "responses": {"200": {"description": "OK"}}},
            "patch":  {"summary": f"Update {e.name}", "responses": {"200": {"description": "OK"}}},
            "delete": {"summary": f"Delete {e.name}", "responses": {"204": {"description": "No Content"}}},
        }
    openapi = {
        "openapi": "3.1.0",
        "info": {"title": spec.app_name, "version": spec.version},
        "paths": paths,
    }
    json.dump(openapi, out, indent=2)
