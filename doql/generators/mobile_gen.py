def _gen_icons(out: pathlib.Path, spec: DoqlSpec) -> None:
    icons_dir = out / "icons"
    icons_dir.mkdir(exist_ok=True)
    letter = (spec.app_name or "D")[0].upper()
    for size in (192, 512):
        svg = textwrap.dedent(f"""\
            <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 {size} {size}">
              <rect width="{size}" height="{size}" rx="{size // 5}" fill="#0ea5e9"/>
              <text x="50%" y="54%" text-anchor="middle"
                    font-family="system-ui, sans-serif"
                    font-size="{int(size * 0.55)}" font-weight="700"
                    fill="white" dominant-baseline="middle">{letter}</text>
            </svg>
            """)
        (icons_dir / f"icon-{size}.svg").write_text(svg, encoding="utf-8")


def generate(spec: DoqlSpec, env_vars: dict[str, str], out: pathlib.Path) -> None:
    """Generate mobile PWA into *out* directory."""
    # Skip generation if no mobile interface is declared
    mobile_iface = next((i for i in spec.interfaces if i.type in ("pwa", "mobile", "mobile-pwa")), None)
    if not mobile_iface and not spec.entities:
        (out / "README.md").write_text(
            f"# {spec.app_name} — Mobile\n\n"
            "No mobile/PWA interface declared and no entities — skipping.\n",
            encoding="utf-8",
        )
        print("    → mobile/README.md (skipped)")
        return

    out.mkdir(parents=True, exist_ok=True)
    (out / "manifest.json").write_text(_gen_manifest(spec), encoding="utf-8")
    (out / "service-worker.js").write_text(_gen_service_worker(spec), encoding="utf-8")
    (out / "index.html").write_text(_gen_index_html(spec), encoding="utf-8")
    (out / "app.js").write_text(_gen_app_js(spec), encoding="utf-8")
    (out / "style.css").write_text(_gen_style_css(), encoding="utf-8")
    _gen_icons(out, spec)

    (out / "README.md").write_text(
        f"# {spec.app_name} — Mobile (PWA)\n\n"
        f"Generated by `doql build`. Installable PWA with offline support.\n\n"
        f"## Quick start\n\n"
        f"```bash\n"
        f"cd build/mobile\n"
        f"# Serve over HTTP (service worker requires http/https, not file://)\n"
        f"python3 -m http.server 8090\n"
        f"# Open http://localhost:8090/ in Chrome/Firefox\n"
        f"# On Android: 'Install app' from the address bar menu.\n"
        f"```\n\n"
        f"## Connecting to the API\n\n"
        f"By default the PWA calls `/api/v1/...` on the same origin. To point\n"
        f"at a different API (e.g. during development), set before loading app.js:\n\n"
        f"```html\n<script>window.DOQL_API_BASE = 'http://localhost:8000';</script>\n```\n\n"
        f"## Production\n\n"
        f"Serve `build/mobile/` behind the same Traefik / nginx that hosts the API,\n"
        f"so `/api/v1` is same-origin and offline cache works as expected.\n",
        encoding="utf-8",
    )
    print("    → mobile/manifest.json")
    print("    → mobile/service-worker.js")
    print("    → mobile/index.html, app.js, style.css")
    print("    → mobile/icons/icon-192.svg, icon-512.svg")
    print("    → mobile/README.md")
