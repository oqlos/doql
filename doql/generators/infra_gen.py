def _gen_terraform(spec: DoqlSpec, env_vars: dict[str, str], out: pathlib.Path) -> None:
    slug = _slug(spec.app_name)
    main_tf = textwrap.dedent(f'''\
        # Generated by doql build — Terraform infrastructure
        terraform {{
          required_providers {{
            docker = {{
              source  = "kreuzwerker/docker"
              version = "~> 3.0"
            }}
          }}
        }}

        provider "docker" {{}}

        resource "docker_image" "{slug}" {{
          name = "{slug}:latest"
          keep_locally = true
        }}

        resource "docker_container" "{slug}" {{
          name  = "{slug}-api"
          image = docker_image.{slug}.name
          ports {{
            internal = 8000
            external = 8000
          }}
          env = [
            "APP_NAME={spec.app_name}",
          ]
        }}
    ''')
    (out / "main.tf").write_text(main_tf, encoding="utf-8")
    print(f"    → infra/main.tf")

    variables_tf = textwrap.dedent("""\
        variable "domain" {
          description = "Application domain"
          type        = string
          default     = "localhost"
        }

        variable "replicas" {
          description = "Number of replicas"
          type        = number
          default     = 1
        }
    """)
    (out / "variables.tf").write_text(variables_tf, encoding="utf-8")
    print(f"    → infra/variables.tf")

    outputs_tf = textwrap.dedent(f'''\
        output "container_name" {{
          value = docker_container.{slug}.name
        }}
    ''')
    (out / "outputs.tf").write_text(outputs_tf, encoding="utf-8")
    print(f"    → infra/outputs.tf")


# ────────────────────────────────────────────────────────
# Nginx
# ────────────────────────────────────────────────────────

def _gen_nginx(spec: DoqlSpec, env_vars: dict[str, str], out: pathlib.Path) -> None:
    slug = _slug(spec.app_name)
    domain = spec.domain or env_vars.get("DOMAIN", f"{slug}.localhost")
    upstream = "localhost:8000"
    tls_block = ""
    for ing in spec.ingresses:
        if ing.tls:
            tls_block = textwrap.dedent(f"""\
                listen 443 ssl;
                ssl_certificate /etc/nginx/ssl/{slug}.crt;
                ssl_certificate_key /etc/nginx/ssl/{slug}.key;
            """)
            break

    conf = textwrap.dedent(f'''\
        # Generated by doql build — Nginx reverse proxy
        upstream {slug}_api {{
            server {upstream};
        }}

        server {{
            listen 80;
            server_name {domain};
            {tls_block}

            location / {{
                proxy_pass http://{slug}_api;
                proxy_set_header Host $host;
                proxy_set_header X-Real-IP $remote_addr;
                proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
                proxy_set_header X-Forwarded-Proto $scheme;
            }}
        }}
    ''')
    (out / "nginx.conf").write_text(conf, encoding="utf-8")
    print(f"    → infra/nginx.conf")

    dockerfile_nginx = textwrap.dedent("""\
        FROM nginx:alpine
        COPY nginx.conf /etc/nginx/conf.d/default.conf
        EXPOSE 80 443
    """)
    (out / "Dockerfile.nginx").write_text(dockerfile_nginx, encoding="utf-8")
    print(f"    → infra/Dockerfile.nginx")


def _gen_migration_spec(spec: DoqlSpec, env_vars: dict[str, str], out: pathlib.Path) -> None:
    """Generate redeploy migration.yaml alongside docker-compose/quadlet artifacts.

    Allows ``redeploy run`` (no args) to deploy what ``doql build`` generated.
    Requires: pip install doql[deploy]  (installs redeploy>=0.1.2)
    """
    import yaml as _yaml

    slug = _slug(spec.app_name)
    domain = spec.domain or env_vars.get("DOMAIN", f"{slug}.example.com")
    host = env_vars.get("DEPLOY_HOST", "local")
    version = spec.version or "0.1.0"
    target_strategy = _map_deploy_strategy(spec.deploy.target)
    verify_url = (
        env_vars.get("VERIFY_URL")
        or (f"https://{domain}/api/health" if host != "local" else f"http://localhost:8000/api/health")
    )

    migration: dict = {
        "name": slug,
        "description": f"Deploy {spec.app_name} — generated by doql build",
        "source": {
            "strategy": "docker_full",
            "host": "local",
            "app": slug,
            "version": version,
        },
        "target": {
            "strategy": target_strategy,
            "host": host,
            "app": slug,
            "version": version,
            "domain": domain,
            "remote_dir": env_vars.get("REMOTE_DIR", f"/opt/{slug}"),
            "env_file": ".env",
            "verify_url": verify_url,
            "verify_version": version[:3],
        },
        "notes": [
            f"Generated by doql build from {spec.app_name} DEPLOY: target: {spec.deploy.target}",
            f"Run: redeploy run --plan-only   (preview steps)",
            f"Run: redeploy run --dry-run     (dry run)",
            f"Run: redeploy run               (deploy!)",
            f"Set DEPLOY_HOST=user@VPS_IP before building to embed VPS address.",
        ],
    }

    # For kiosk: point to the install script generated by _gen_kiosk
    if spec.deploy.target == "kiosk-appliance":
        migration["extra_steps"] = [
            {
                "id": "run_kiosk_installer",
                "action": "ssh_cmd",
                "description": "Run doql-generated kiosk install script",
                "command": f"bash /opt/{slug}/infra/install-kiosk.sh",
                "risk": "medium",
            }
        ]

    dest = out / "migration.yaml"
    dest.write_text(_yaml.dump(migration, allow_unicode=True, sort_keys=False), encoding="utf-8")
    print(f"    → infra/migration.yaml  (redeploy spec)")


def generate(spec: DoqlSpec, env_vars: dict[str, str], out: pathlib.Path) -> None:
    """Generate infra layer files into *out* directory."""
    # Prefer explicit Infrastructure blocks over legacy deploy.target
    infra_types = {i.type for i in spec.infrastructures}
    ingress_types = {i.type for i in spec.ingresses}

    # Fallback to legacy deploy.target if no Infrastructure blocks declared
    if not infra_types:
        infra_types = {spec.deploy.target}

    for itype in infra_types:
        if itype in ("docker-compose", "docker"):
            _gen_docker_compose(spec, env_vars, out)
        elif itype == "quadlet":
            _gen_quadlet(spec, env_vars, out)
        elif itype == "kiosk-appliance":
            _gen_kiosk(spec, env_vars, out)
        elif itype in ("kubernetes", "k8s", "k3s"):
            _gen_kubernetes(spec, env_vars, out)
        elif itype == "terraform":
            _gen_terraform(spec, env_vars, out)
        else:
            _gen_docker_compose(spec, env_vars, out)

    # Nginx config if requested via Ingress block
    if "nginx" in ingress_types:
        _gen_nginx(spec, env_vars, out)

    # Always emit migration.yaml for redeploy (doql[deploy])
    _gen_migration_spec(spec, env_vars, out)
