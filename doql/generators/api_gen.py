"""Generate FastAPI + SQLAlchemy backend from DoqlSpec.

Produces:
  api/
  ├── main.py           — FastAPI app + CORS + lifespan
  ├── database.py       — SQLAlchemy engine + session
  ├── models.py         — ORM models from ENTITY
  ├── schemas.py        — Pydantic request/response schemas
  ├── routes.py         — CRUD endpoints for each ENTITY
  ├── requirements.txt  — pinned deps
  └── README.md
"""
from __future__ import annotations

import json
import pathlib
import textwrap
from typing import IO

from ..parser import DoqlSpec, Entity, EntityField


# ────────────────────────────────────────────────────────
# Type mapping: doql type → (SQLAlchemy, Python/Pydantic)
# ────────────────────────────────────────────────────────

_TYPE_MAP: dict[str, tuple[str, str]] = {
    "uuid":     ("String(36)",  "str"),
    "string":   ("String(255)", "str"),
    "text":     ("Text",        "str"),
    "int":      ("Integer",     "int"),
    "float":    ("Float",       "float"),
    "bool":     ("Boolean",     "bool"),
    "date":     ("Date",        "date"),
    "datetime": ("DateTime",    "datetime"),
    "json":     ("JSON",        "dict"),
    "image":    ("String(512)", "str"),
    "pdf":      ("String(512)", "str"),
    "oql":      ("String(255)", "str"),
    "enum":     ("String(64)",  "str"),
}


def _sa_type(f: EntityField) -> str:
    return _TYPE_MAP.get(f.type, ("String(255)", "str"))[0]


def _py_type(f: EntityField) -> str:
    base = _TYPE_MAP.get(f.type, ("", "str"))[1]
    if f.ref:
        return "str"
    if not f.required and not f.auto:
        return f"Optional[{base}]"
    return base


def _py_default(f: EntityField) -> str:
    if f.auto:
        return " = None"
    if f.default:
        val = f.default
        if f.type == "bool":
            return f" = {val.capitalize()}"
        if f.type == "int":
            return f" = {val}"
        return f' = "{val}"'
    if not f.required:
        return " = None"
    return ""


_PY_KEYWORDS = frozenset({
    "False", "None", "True", "and", "as", "assert", "async", "await",
    "break", "class", "continue", "def", "del", "elif", "else", "except",
    "finally", "for", "from", "global", "if", "import", "in", "is",
    "lambda", "nonlocal", "not", "or", "pass", "raise", "return",
    "try", "while", "with", "yield", "type",
})


def _safe_name(name: str) -> str:
    """Escape Python reserved keywords by appending underscore."""
    return f"{name}_" if name in _PY_KEYWORDS else name


def _snake(name: str) -> str:
    import re
    s1 = re.sub("(.)([A-Z][a-z]+)", r"\1_\2", name)
    return re.sub("([a-z0-9])([A-Z])", r"\1_\2", s1).lower()


